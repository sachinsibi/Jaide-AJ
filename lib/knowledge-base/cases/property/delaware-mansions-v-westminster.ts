  content: `Delaware Mansions Ltd v Westminster City Council [2001] UKHL 55.

Facts: A plane tree on a Westminster-maintained footway grew over many years; its roots encroached beneath a block of flats and caused structural cracking by abstracting moisture from clay soil beneath the foundations. Delaware Mansions bought the block after cracking had begun. They notified the council and asked for the tree's removal; the council declined. Delaware Mansions undertook substantial underpinning and sued for the cost.

Held (HL, unanimous): Westminster was liable. Tree-root encroachment causing physical damage to a building is a continuing nuisance. Once the council had notice and a reasonable opportunity to abate (by felling or root pruning) and refused, it was liable for the reasonable cost of remedial works the claimant had to undertake to prevent further damage.

Lord Cooke's analysis:

(1) Continuing nuisance: where ongoing physical damage occurs day by day, every day of continuance gives a fresh cause of action. Limitation does not extinguish a remedy for damage continuing within 6 years of the claim.

(2) Recoverability of remedial-works cost: the affected owner recovers reasonable costs of works necessary to prevent further damage — underpinning, root barriers, repair of cracks — provided the cost is reasonable and the works necessary.

(3) Notice and the new owner: a successor in title can sue for continuing damage that began before purchase, provided notice has been given and abatement refused. Smith v Scott [1973] (no liability for nuisance created by previous owner where current owner inherits without knowledge) is qualified: liability arises once the present defendant knows or ought to know.

(4) Reasonable opportunity to abate: the defendant must be given fair time to consider and act. Refusal or unreasonable delay crystallises liability for subsequent costs.

Doctrinal contribution: continuing-nuisance principle applied to tree-root encroachment; cost of self-help / remedial works recoverable in nuisance, including cost of preventing future damage; in continuing-nuisance cases, plead post-notice continuance (always within time as fresh accruals).

Application: where a neighbour's or council's tree is causing subsidence, get an arboricultural / structural report; serve notice with a reasonable abatement period (typically 14 days emergency, 28 days non-emergency). If refused, the affected owner may proceed with reasonable remedial works and sue for cost. Insurance subrogation: the building insurer is often the de facto claimant.`,
