  content: `Nettleship v Weston [1971] 2 QB 691 (Court of Appeal).

Facts: The defendant, Mrs Weston, was a learner driver receiving instruction from a friend, Mr Nettleship. On her third lesson, she lost control of the car, mounted the kerb, and struck a lamp post. Mr Nettleship suffered a broken kneecap. She admitted the failure was due to inexperience but argued she should be judged by the standard of a reasonable learner, not a fully competent driver.

Held (CA): Mrs Weston was liable. The standard of care expected of every driver is the objective standard of the reasonably competent and experienced driver. The standard does not vary according to the driver's experience, skill, age, or familiarity with the vehicle.

Lord Denning MR: "The learner driver may be doing his best, but his incompetent best is not good enough. He must drive in as good a manner as a driver of skill, experience and care, who is sound in mind and limb, who makes no errors of judgment, has good eyesight and hearing, and is free from any infirmity."

Salmon LJ added that a driver who is overcome by sudden illness or some other unanticipated incapacity may have a defence (automatism), but ordinary inexperience is not such an incapacity.

Contributory negligence: the Court held that Mr Nettleship, knowing he was teaching a learner, was contributorily negligent only to a limited extent (50% reduction was rejected; the case was decided on a small reduction reflecting his consent to be a passenger, which the modern court would handle differently).

Application: Nettleship is the leading authority for the proposition that the standard of care in motor negligence is objective and uniform:

(a) Learner drivers — judged as fully competent drivers.
(b) Newly qualified drivers — judged as fully competent drivers.
(c) Drivers unfamiliar with a particular vehicle — judged as fully competent drivers of that vehicle.
(d) Drivers with reduced reaction times due to fatigue (without medical incapacity) — judged as fully competent drivers.

The objective standard is mediated only by genuine medical incapacity arising suddenly without prior warning (Mansfield v Weetabix [1998] 1 WLR 1263) and by emergency circumstances where a reasonable driver would have only seconds to respond (the "agony of the moment" doctrine).`,
